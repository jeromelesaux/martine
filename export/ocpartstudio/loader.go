func EgxLoader(filePath string, p color.Palette, mode1, mode2 uint8, cfg *config.MartineConfig) error {
	var out string
	for i := 0; i < len(p); i++ {
		v, err := constants.FirmwareNumber(p[i])
		if err == nil {
			out += fmt.Sprintf("%0.2d", v)
		} else {
			log.GetLogger().Error("Error while getting the hardware values for color %v, error :%v\n", p[0], err)
		}
		if i+1 < len(p) {
			out += ","
		}

	}

	filename := cfg.GetAmsdosFilename(filePath, "")
	var loader []byte
	if cfg.CpcPlus {
		loader = egxPlusBasicLoader
		copy(loader[startPlusScreenName:], filename[:])

	} else {
		loader = egxLoaderBasic
		copy(loader[startPaletteValues:], out[0:])
		copy(loader[startPaletteName:], filename[:])
		copy(loader[startScreenName:], filename[:])
	}

	log.GetLogger().Infoln(loader)
	osFilepath := cfg.AmsdosFullPath(filePath, ".BAS")

	if !cfg.NoAmsdosHeader {
		if err := amsdos.SaveAmsdosFile(osFilepath, ".BAS", loader, 0, 0, 0x170, 0); err != nil {
			return err
		}
	} else {
		if err := amsdos.SaveOSFile(osFilepath, loader); err != nil {
			return err
		}
	}

	cfg.AddFile(osFilepath)
	var egxLoader []byte

	var egxHeaderStartAddress uint16
	if !cfg.CpcPlus {
		egxLoader = egxBinary
		pal := make([]byte, 16)
		index := 15
		for i := 0; i < len(p); i++ {
			v, err := constants.HardwareValues(p[i])
			if err != nil {
				log.GetLogger().Error("Error while getting the hardware values for color %v, error :%v\n", p[i], err)
			}
			pal[index] = v[0]
			index--
		}
		copy(egxLoader[egxBinaryPaletteOffset:], pal)
		switch mode2 {
		case 0:
			egxLoader[egxMode1Offset] = 0x8c
		case 1:
			egxLoader[egxMode1Offset] = 0x8d
		case 2:
			egxLoader[egxMode1Offset] = 0x8e
		}

		switch mode1 {
		case 0:
			egxLoader[egxMode2Offset] = 0x8c
		case 1:
			egxLoader[egxMode2Offset] = 0x8d
		case 2:
			egxLoader[egxMode2Offset] = 0x8e
		}
		egxHeaderStartAddress = 0x3000
	} else {
		egxLoader = egxPlusBinary
		kit := make([]byte, 32)
		index := 0
		for i := 0; i < len(p); i++ {
			cp := constants.NewCpcPlusColor(p[i])
			b := cp.Bytes()
			kit[index] = b[0]
			index++
			kit[index] = b[1]
			index++
		}
		copy(egxLoader[egxPlusBinaryPaletteOffset:], kit[:])
		log.GetLogger().Infoln(kit)
		switch mode2 {
		case 0:
			egxLoader[egxPlusMode1Offset] = 0x8c
		case 1:
			egxLoader[egxPlusMode1Offset] = 0x8d
		case 2:
			egxLoader[egxPlusMode1Offset] = 0x8e
		}

		switch mode1 {
		case 0:
			egxLoader[egxPlusMode2Offset] = 0x8c
		case 1:
			egxLoader[egxPlusMode2Offset] = 0x8d
		case 2:
			egxLoader[egxPlusMode2Offset] = 0x8e
		}
		egxHeaderStartAddress = 0x8000
	}

	egxBinPath := filepath.Join(cfg.OutputPath, "EGX.BIN")

	if !cfg.NoAmsdosHeader {
		if err := amsdos.SaveAmsdosFile(egxBinPath, ".BIN", egxLoader, 2, 0, egxHeaderStartAddress, egxHeaderStartAddress); err != nil {
			return err
		}
	} else {
		if err := amsdos.SaveOSFile(egxBinPath, egxLoader); err != nil {
			return err
		}
	}

	cfg.AddFile(egxBinPath)

	return nil
}
