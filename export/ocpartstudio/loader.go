	filename = amsdos.AmsdosFilename(filename, ".SCR")
	var scrFile [12]byte
	for i := 0; i < 12; i++ {
		scrFile[i] = ' '
	}
	i := strings.Index(filename, ".")
	copy(scrFile[0:], filename[0:i])
	copy(scrFile[8:], filename[i:])
	copy(loader[startScreenPlusName:], scrFile[:])
